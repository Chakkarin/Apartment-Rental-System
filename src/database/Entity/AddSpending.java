package database.Entity;

public class AddSpending {
    private String months;
    private String years;
    private String per;
    private String unit;
    private String dateReal;
    private String charges_id;
    private String idlicense;
    private String roomid;
    private String idrender;
    private String idreceipt;

    public AddSpending() {
    }

    public String getMonths() {
        return months;
    }

    public void setMonths(String months) {
        this.months = months;
    }

    public String getYears() {
        return years;
    }

    public void setYears(String years) {
        this.years = years;
    }

    public String getPer() {
        return per;
    }

    public void setPer(String per) {
        this.per = per;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getDateReal() {
        return dateReal;
    }

    public void setDateReal(String dateReal) {
        this.dateReal = dateReal;
    }

    public String getCharges_id() {
        return charges_id;
    }

    public void setCharges_id(String charges_id) {
        this.charges_id = charges_id;
    }

    public String getIdlicense() {
        return idlicense;
    }

    public void setIdlicense(String idlicense) {
        this.idlicense = idlicense;
    }

    public String getRoomid() {
        return roomid;
    }

    public void setRoomid(String roomid) {
        this.roomid = roomid;
    }

    public String getIdrender() {
        return idrender;
    }

    public void setIdrender(String idrender) {
        this.idrender = idrender;
    }

    public String getIdreceipt() {
        return idreceipt;
    }

    public void setIdreceipt(String idreceipt) {
        this.idreceipt = idreceipt;
    }
    
}
